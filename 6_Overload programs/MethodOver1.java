
package abc;


 class MethodOver1 
 {
    public void show(int i)
    {
        System.out.println("Inside show() with int argument : "+i);
    }
    public void show(char c)
    {
        System.out.println("Inside show() with char argument : "+c);
    }    
    public void show(String s)
    {
        System.out.println("Inside show() with string argument : "+s);
    }
    public void show(float f)
    {
        System.out.println("Inside show() with float argument : "+f);
    }
 }
